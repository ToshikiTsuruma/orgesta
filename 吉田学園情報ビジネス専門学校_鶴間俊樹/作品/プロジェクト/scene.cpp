//=============================================================================
// シーンの変更
//=============================================================================
void CScene::ChangeScene(CScene*& pScene, SCENE_TYPE typeScene) {
	//現在のシーンの破棄
	if (pScene != nullptr) {
		pScene->Uninit();
		delete pScene;
		pScene = nullptr;
	}

	//新しいシーンの生成
	switch (typeScene)
	{
	case CScene::SCENE_TYPE::TITLE:
		pScene = new CTitleScene;
		if (pScene != nullptr) pScene->Init();
		break;

	case CScene::SCENE_TYPE::GAME:
		pScene = new CGameScene;
		if (pScene != nullptr) pScene->Init();
		break;
	}
}